								<h1>Author Guidelines</h1>
								<p>The Nigeria Association of Clinical Psychology journal portal aims to publish original research, comprehensive review articles, short reports and commentaries on clinical psychology. The journal strives to provide a forum for Nigerian and African Authors as well as others working in Africa, to share findings on all aspects of clinical psychology and to disseminate innovative, relevant and useful information on clinical psychology throughout the continent.</p>



 




									<p>The journal will publish original research, review articles, short reports and commentaries. A cover page should accompany each manuscript and should include:</p>

					
								<ul class="tg-ullist tg-liststyledot">
									<li>The Title and sub-title (running title -- no more than 5 words);</li>
									<li>The name(s) of the author(s);</li>
									<li>The affiliation of the author(s);</li>
									<li>Abstract; </li>
									<li>Three to six Keywords for indexing and retrieval purposes;</li>
									
								</ul>
								<div class="clearfix"></div>
						<p><b>Original Research — </b>The journal welcomes articles reporting on original research, including both quantitative and qualitative studies. The subject matter should be organized under appropriate headings and sub-headings such as: Introduction, Methods, Results, Discussion, and Acknowledgements.</p>
						
								<p>Maximum length should be 5,000 words together. Figures and/or Tables should not exceed five (5) e.g., 2 figures and 3 tables. Where any Table occupies a full page, no more than 4 tables and/or figures should be included. Additional tables/figures will be charged.</p>

								<p><b>Review Articles —</b> Comprehensive review articles on all aspects of reproductive health in Africa will also be considered for publication in the journal. Reviews should provide a thorough overview of the topic and should incorporate the most current research. The length of review articles and the organizational headings and sub-headings used are at the author’s discretion.</p>

								<p><b>Short Reports — </b>Brief descriptions of preliminary research findings or interesting case studies will be considered for publication as short reports. The length and organization of short reports are left to the author’s discretion.</p>

								<p><b>Commentaries — </b>Commentaries or editorials on any aspect of reproductive health in Africa will be considered for publication in the journal. Opinion pieces need not reference previous research, but rather reflect the opinions of the author(s)</p>
